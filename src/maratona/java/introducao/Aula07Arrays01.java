public class Aula07Arrays01 {
    public static void main(String[] args) {
        int [] idade = new int[5];
        idade[0] = 10;
        idade[1] = 1;
        idade[2] = 4;
        System.out.println(idade.length);
        System.out.println(idade[0]);
        System.out.println(idade[1]);
        System.out.println(idade[2]);
    }
}
